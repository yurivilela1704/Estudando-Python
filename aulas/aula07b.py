n1 = int(input('Um valor: '))
n2 = int(input('Outro valor'))

sum = n1 + n2
multi = n1 * n2
division = n1 / n2
div = n1 // n2
rest = n1 % n2
exp = n1 ** n2

print(f'A soma dos valores dado é {sum}')
print(f'A multiplilcação dos valores dados é {multi}')
print(f'A divisão é {division}')
print(f'A divisão int do valor é {div}')
print(f'O modulo da divisao é {rest}')
print(f'A potencia de {n1} sobre {n2} é {exp}')
