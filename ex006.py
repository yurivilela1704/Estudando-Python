# Faça um programa que leia um numero e mostre o seu dobro, triplo e raiz quadrada
algoritimo = int(input('Digite um número: '))
dobronum = algoritimo * 2
triplonum = algoritimo * 3
raiznum = algoritimo ** 1/2

print(f'O dobro de {algoritimo} é {dobronum}, triplo é {triplonum} e a raíz quadrada é {raiznum}')
